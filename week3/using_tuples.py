#tuples are immutable

my_tuple = (1, 2, 3, 4, 5)

#tuples can be accessed and used with bracket notation
print(my_tuple[0] + my_tuple[1])

#If you don't know how long a tuple is but want to know the last index use negatives
print(my_tuple[-1])

# or use len
print(len(my_tuple))

#Like variables tuples can be reassigned
my_tuple = (5, 4, 3, 2, 1)
print(my_tuple)

